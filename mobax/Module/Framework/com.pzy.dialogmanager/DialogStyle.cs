public enum DialogStyle
{
    /// <summary>
    /// 仅有确认按钮
    /// </summary>
    ConfirmOnly,

    /// <summary>
    /// 有确认取消按钮
    /// </summary>
    ComfirmCancel,

    /// <summary>
    /// 无按钮，点击任何地方关闭
    /// </summary>
    NoButton,

    AskToBy,

    /// <summary>
    /// 异常提示框，需要提供额外的异常信息
    /// </summary>
    ExceptionReport,
    SilentableAsk,

    /// <summary>
    /// 功能开启专用
    /// </summary>
    SystemOpen,

    /// <summary>
    /// 广告
    /// </summary>
    Ad,
    
}
